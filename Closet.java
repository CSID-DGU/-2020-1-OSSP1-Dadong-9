package closet;

public class Closet {
	private String userID; 
	private int clothCategory;
	private int clothAvailable;
 
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getClothCategory() {
		return clothCategory;
	}
	public void setClothCategory(int clothCategory) {
		this.clothCategory = clothCategory;
	}
	public int getClothAvailable() {
		return clothAvailable;
	}
	public void setClothAvailable(int clothAvailable) {
		this.clothAvailable = clothAvailable;
	}
	 
	}
