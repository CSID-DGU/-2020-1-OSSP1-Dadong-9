package closet;

public class Closet {
	private String userID; 
	private String clothCategory;
	private String clothAvailable;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getClothCategory() {
		return clothCategory;
	}
	public void setClothCategory(String clothCategory) {
		this.clothCategory = clothCategory;
	}
	public String getClothAvailable() {
		return clothAvailable;
	}
	public void setClothAvailable(String clothAvailable) {
		this.clothAvailable = clothAvailable;
	}
 

	}
